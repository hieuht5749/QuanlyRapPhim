    public void loadDataToTableVe() {
        list = VeDAO.getAll();
        listmovies = mvDao.getAll();
        listnv = nvDao.getAll();
        listP = PhongDao.getAll();
        listghe = gheDao.getAll();
        String[] columnNames = {"Mã Hóa Đơn"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        for (Ve v : list) {
            model.addRow(new Object[]{v.getMahoadon()});
        }
        tblHoaDon.setModel(model);
    }

    public void fillToFrom() {
        int index = tblHoaDon.getSelectedRow();
        Ve v = list.get(index);
        txtMaHD.setText(v.getMahoadon());
        txtMAKH.setText(v.getMakhachhang());
        TXTMANV.setText(v.getManv());
        TXTMAPHIM.setText(v.getMaphim());
        TXTTENpHIM.setText(v.getTenphim());
        TXTsOLUONGVE.setText(String.valueOf(v.getSove()));
        if (v.getSuatchieu().equals("7:45-9:45")) {
            RDO79.setSelected(true);
        } else if (v.getSuatchieu().equals("9:45-11:45")) {
            RDO911.setSelected(true);
        } else if (v.getSuatchieu().equals("7:11:45-13:45")) {
            RDO1113.setSelected(true);
        } else if (v.getSuatchieu().equals("13:45-15:45")) {
            RDO1315.setSelected(true);
        } else if (v.getSuatchieu().equals("15:45-17:45")) {
            RDO1517.setSelected(true);
        } else if (v.getSuatchieu().equals("17:45-1945")) {
            RDO1719.setSelected(true);
        } else if (v.getSuatchieu().equals("19:45-21:45")) {
            RDO1921.setSelected(true);
        } else if (v.getSuatchieu().equals("21:45-23:45")) {
            RDO2123.setSelected(true);
        }

        TXTPHONG.setText(v.getPhong());
        TXTMAGHE.setText(v.getMaghe());
        TXTHANG.setText(v.getSohang());
        TXTCOT.setText(v.getSocot());
        SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
        String rDate = df.format(v.getNgaymua());
        TXTNGAYBAN.setText(rDate);
        TXTTONGTIEN.setText(String.valueOf(v.getTongtien()));

    }

