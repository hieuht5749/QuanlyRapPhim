    public boolean checkfrom() {

        if (txtMaHD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã hóa đơn");
            return false;
        }
        for (Ve ve : list) {
            if (txtMaHD.getText().equals(ve.getMahoadon())) {
                JOptionPane.showMessageDialog(null, "Mã hóa đơn đã tồn tại");
                return false;
            }
        }
        if (txtMAKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã khách hàng");
            return false;
        }
        if (TXTMANV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã nhân viên");
            return false;
        }

        if (checkmanv() == false) {
            return false;
        }
        if (TXTMAPHIM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã phim");
            return false;
        }
        if (TXTTENpHIM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên phim");
            return false;
        }

        if (checkmaPhim() == false) {
            return false;
        }
        if (TXTsOLUONGVE.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập số vé phim");
            return false;
        }
        try {
            int ve = Integer.parseInt(TXTsOLUONGVE.getText());
            if (ve < 1) {
                JOptionPane.showMessageDialog(null, "Bạn phải mua ít nhất một vé");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Vé phải là số");
            return false;
        }
        boolean suatChieu = RDO79.isSelected() || RDO911.isSelected() || RDO1113.isSelected() || RDO1315.isSelected() || RDO1517.isSelected() || RDO1719.isSelected() || RDO1921.isSelected() || RDO2123.isSelected();
        if (suatChieu == false) {
            JOptionPane.showMessageDialog(null, "Hãy chọn xuất chiếu");
        }
        if (TXTPHONG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã phòng");
            return false;
        }

        if (checkPhong() == false) {
            return false;
        }
        if (TXTMAGHE.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã ghế");
            return false;
        }

        if (checkghe() == false) {
            return false;
        }
        if (checkghe2() == false) {
            return false;
        }

        if (TXTNGAYBAN.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập ngày bán vé");
            return false;
        }
        return true;
    }

