    public boolean checkMap() {
        for (Phong phong : listphong) {
            if (txtSDPhong.getText().equals(phong.getMa())) {
                return true;
            }
        }
        JOptionPane.showMessageDialog(null, "Phòng không tồn tại");
        return false;
    }

    public void addtb() {
        if (checkfrom()) {
            Thietbi thietbi = new Thietbi();
            thietbi.setMatb(txtmatb.getText());
            thietbi.setTentb(txtTentb.getText());
            thietbi.setSoluongtb(Integer.parseInt(txtSoLuong.getText()));
            thietbi.setMotatb(txtMota.getText());
            thietbi.setMaphong(txtSDPhong.getText());
            int rs = ThietBiDAO.insert(thietbi);
            loadDataToTable();
            if (rs > 0) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");

            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại");
            }
        }
    }

    public void UpdateTB() {
        if (checkfromUPDATE()) {
            for (Thietbi thietbi : list) {
                if (txtmatb.getText().equals(thietbi.getMatb())) {
                    thietbi.setMatb(txtmatb.getText());
                    thietbi.setTentb(txtTentb.getText());
                    thietbi.setSoluongtb(Integer.parseInt(txtSoLuong.getText()));
                    thietbi.setMotatb(txtMota.getText());
                    thietbi.setMaphong(txtSDPhong.getText());
                    int rs = ThietBiDAO.update(thietbi);
                    loadDataToTable();
                    if (rs > 0) {
                        JOptionPane.showMessageDialog(null, "Thêm thành công");

                    } else {
                        JOptionPane.showMessageDialog(null, "Thêm thất bại");
                    }
                }
            }

        }
    }

    public void newfrom() {
        txtmatb.setText("");
        txtTentb.setText("");
        txtSoLuong.setText("");
        txtMota.setText("");
        txtSDPhong.setText("");
    }

    public void xoaTB() {
        String ma = txtmatb.getText();
        if (txtmatb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã thiết bị");
            return;
        }
        if (checkmatb3()) {
            for (Thietbi thietbi : list) {
                if (ma.equals(thietbi.getMatb())) {
                    int rs = ThietBiDAO.delet(thietbi);
                    loadDataToTable();
                    newfrom();
                    if (rs > 0) {
                        JOptionPane.showMessageDialog(null, "Xóa thành công");

                    } else {
                        JOptionPane.showMessageDialog(null, "Xóa thất bại");
                    }
                    break;
                }
            }
        }

    }

