   public void loadDataToTable() {
        list = ThietBiDAO.getAll();
        listphong = PhongDao.getAll();
        String[] columnNames = {"Mã", "Tên ", "Số Lượng", "Mô Tả", "Sử Dụng Tại Phòng"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
        for (Thietbi tb : list) {
            model.addRow(new Object[]{tb.getMatb(), tb.getTentb(), tb.getSoluongtb(), tb.getMotatb(), tb.getMaphong()});
        }
        tblThietBi.setModel(model);
    }

    public void fillToFrom() {
        int index = tblThietBi.getSelectedRow();
        Thietbi thietbi = list.get(index);
        txtmatb.setText(thietbi.getMatb());
        txtTentb.setText(thietbi.getTentb());
        txtSoLuong.setText(String.valueOf(thietbi.getSoluongtb()));
        txtMota.setText(thietbi.getMotatb());
        txtSDPhong.setText(thietbi.getMaphong());
    }

    public boolean checkfrom() {
        if (txtmatb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã thiết bị");
            return false;
        }
        if (checkmatb() == false) {
            return false;
        }
        if (txtTentb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên thiết bị");
            return false;
        }
        if (txtSoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập số lượng thiết bị");
            return false;
        }
        try {
            int sl = Integer.parseInt(txtSoLuong.getText());
            if (sl < 1) {
                JOptionPane.showMessageDialog(null, "Số lượng không được nhỏ hơn một");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Số lượng phải là số");
            return false;
        }
        if (txtMota.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mô tả thiết bị");
            return false;
        }
        if (txtSDPhong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa mã phòng đặt thiết bị");
            return false;
        }
        if (checkMap() == false) {
            return false;
        }
        return true;
    }

